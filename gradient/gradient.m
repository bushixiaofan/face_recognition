% --- Executes on button press in pushbutton4.
function pushbutton4_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton4 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
global path;
global file;
global X;
global Y;
global test;
global result;
global class1;
global class2;
image1 = strcat(path,'\1\1.bmp');
image2 = strcat(path,'\2\1.bmp');
for p = 1:2
    if p ==1
        f = strcat(path,'\1\');
    else
        f = strcat(path,'\2\');
    end
    files=dir(strcat(f,'*.bmp'));   %Read files information in the folder
    number = numel(files);  %the number of the files in the select path
    for i = 1:number
        images{i}=imread(strcat(f,files(i).name));
        image = images{i};
        [m,n] = size(image);
        for j = 1:m
            for k =1:n
                if p == 2
                    X(i+number,(j-1)*n+k) = image(j,k)/155;  %归一化，将其灰度级归一化为0―1之间
                else
                    X(i,(j-1)*n+k) = image(j,k)/155;
                end
            end
        end
        if p == 1
            Y(i) = 1;      %属于第一类
        else
            Y(i+number) = -1;  %属于第二类
        end
    end
end
disp('Please wait a moment......');
a = train(X,Y);  %通过测试样本获得权向量，梯度下降算法的核心部分
files=dir(strcat(file,'\*.bmp'));   %Read files information in the folder
number = numel(files);  %the number of the files in the select path
for i = 1:number
    imagess{i} = imread(strcat(file,'\',files(i).name));
    testimage = imagess{i};
    [m,n] = size(testimage);
    for j = 1:m
        for k =1:n
            test(i,(j-1)*n+k) = testimage(j,k)/155;   %归一化，将其灰度级归一化为0―1之间
        end
    end
    result(i) = GradientDescent(a,test(i,1:m*n));     %计算测试样本的判别函数的值
    if result(i) >= 0
        disp(strcat(files(i).name,'---------------The first class!'));
        figure;
        imshow(imagess{i});
        title('The testing image belongs to the first class!');
    else
        disp(strcat(files(i).name,'--------------The second class!'));
        figure;
        imshow(imagess{i});
        title('The testing image belongs to the second class!');
    end
end
disp(result);


