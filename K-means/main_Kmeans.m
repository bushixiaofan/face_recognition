% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
global x;
code=inputdlg('Please input the Value of K:');
k=str2double(cell2mat(code));
[n,d] = size(x);
bn=round(n/k*rand);%第一个随机数在前1/K的范围内
nc = [];
for i = 1:k
     nc = [nc x(i*bn,:)'];
end
nc = nc';%初始聚类中心
subplot(1,2,2);
[cid,nr,centers] = k_means(x,k,nc);%调用kmeans函数

clor = ['r' 'b' 'y' 'g' 'm' 'c'];
if k > size(clor,2)
    m = ceil(k/size(clor,2));
    clor = repmat(clor,1,m);
end
for i=1:150,
    for j = 1:k
        if cid(i)==j,
            plot(x(i,1),x(i,2),strcat(clor(j),'+'));
            hold on;
        end
    end
end
plot(centers(:,1),centers(:,2),'k.');
title('Clustering result');
set(gca,'nextplot','replacechildren');
uiwait(handles.figure1);


